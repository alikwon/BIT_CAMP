package cp3_0410;

public class UnaryAddMin {

	public static void main(String[] args) {
		
		int n1 = 5;				//변수 선언
		
		System.out.println(+n1);//(+)부호를 붙이므로   5출력
		System.out.println(-n1);//(-)부호를 붙이므로 -5출력
		
		short n2 = 7;			//변수 선언
		int n3 = +n2;			// +n2를 대입
		int n4 = -n2;			// -n2를 대입
		
		System.out.println(n3); //  7출력
		System.out.println(n4); // -7출력

	}

}
